def main():
    """Main entry point for CLI usage"""

    parser = argparse.ArgumentParser(
        description='N8N Workflow Generator - Complete Pipeline'
    )
    parser.add_argument(
        '--request', '-r',
        type=str,
        help='Automation request description'
    )
    parser.add_argument(
        '--input', '-i',
        type=str,
        help='Input file containing request'
    )
    parser.add_argument(
        '--output', '-o',
        type=str,
        default='workflow_package',
        help='Output directory for workflow package'
    )

    args = parser.parse_args()

    # Get user request
    if args.request:
        user_request = args.request
    elif args.input:
        with open(args.input, 'r', encoding='utf-8') as f:
            user_request = f.read()
    else:
        print("Error: Please provide --request or --input")
        sys.exit(1)

    # Execute pipeline
    pipeline = WorkflowGeneratorPipeline(output_dir=args.output)
    result = pipeline.execute_pipeline(user_request)

    # Print results
    if result['status'] == 'success':
        print(f"\nSuccess! Workflow package created at: {result['output_directory']}")
        print(f"Summary: {result['summary_file']}")
    else:
        print(f"\nPipeline failed: {result['error']}")
        print(f"Completed phases: {', '.join(result['completed_phases'])}")
        sys.exit(1)


if __name__ == '__main__':
    main()
