    def execute_pipeline(self, user_request: str) -> Dict[str, Any]:
        """Execute complete three-phase pipeline"""

        self.log("Starting complete workflow generation pipeline")
        self.log(f"User request: {user_request}")

        try:
            # Phase 1: Enhance Prompt
            self.log("=" * 60)
            phase1_result = self.phase1_enhance_prompt(user_request)
            self.phase_outputs['phase1'] = phase1_result
            self.log("Phase 1 completed successfully", "PHASE 1")

            # Phase 2: Create Architecture
            self.log("=" * 60)
            phase2_result = self.phase2_create_architecture(phase1_result['content'])
            self.phase_outputs['phase2'] = phase2_result
            self.log("Phase 2 completed successfully", "PHASE 2")

            # Phase 3: Generate JSON
            self.log("=" * 60)
            phase3_result = self.phase3_generate_json(phase2_result)
            self.phase_outputs['phase3'] = phase3_result
            self.log("Phase 3 completed successfully", "PHASE 3")

            # Generate summary
            self.log("=" * 60)
            summary = self._generate_summary()
            summary_file = self.output_dir / "00_PIPELINE_SUMMARY.md"
            with open(summary_file, 'w', encoding='utf-8') as f:
                f.write(summary)

            self.log(f"Pipeline completed successfully!")
            self.log(f"Output package: {self.output_dir}")

            return {
                "status": "success",
                "output_directory": str(self.output_dir),
                "phases": self.phase_outputs,
                "summary_file": str(summary_file)
            }

        except Exception as e:
            self.log(f"Pipeline failed: {str(e)}", "ERROR")
            return {
                "status": "failed",
                "error": str(e),
                "completed_phases": list(self.phase_outputs.keys())
            }

